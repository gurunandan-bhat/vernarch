	function addStateList(event) {
		console.log('Country Changes');
		var country = event.target.value;
		var stateList = states[country];
		var stateSelector = document.getElementById('state');

		stateSelector.options.length = 0;
		if (stateList.length) {
			stateSelector.required = true;
			stateSelector.options[stateSelector.options.length] = new Option('Choose ...', '');
			for (const s of stateList) {
				stateSelector.options[stateSelector.options.length] = new Option(s, s);
			}
		} else {
			stateSelector.required = false;
			stateSelector.options[stateSelector.options.length] = new Option('Not Required', '');
		}
	}
